package com.nirvasoft.web.pos.model;

public class StockMemoData {
	private String syskey;	
	private String t1;// memo
	private String n1;// stock sk
	

	public StockMemoData() {
		clearProperties();
	}

	private void clearProperties() {
		this.syskey = "0";		
		this.t1 = "";
		this.n1 = "0";		
	}

	public String getSyskey() {
		return syskey;
	}

	public void setSyskey(String syskey) {
		this.syskey = syskey;
	}

	public String getT1() {
		return t1;
	}

	public void setT1(String t1) {
		this.t1 = t1;
	}

	public String getN1() {
		return n1;
	}

	public void setN1(String n1) {
		this.n1 = n1;
	}	
}

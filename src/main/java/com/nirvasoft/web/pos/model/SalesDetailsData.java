package com.nirvasoft.web.pos.model;

public class SalesDetailsData {
	private String syskey;
    private String pId;
    private String stkSK;
    private String stkCode;
    private String stkDesc;
    private double price;
    private double qty;
    private String uom;
    private double discAmt;
    private double discPer;
    private double amt;
    private double netAmt;
    private double lineTotal;
    private double cost;    
	
	public SalesDetailsData() {
		clearProperties();
	}
	private void clearProperties() {
		this.syskey = "0";
		this.pId = "";
		this.stkSK = "";
		this.stkCode = "";
		this.stkDesc = "";
		this.uom = "0";
		this.discAmt = 0;
		this.discPer = 0;
		this.amt = 0;
		this.netAmt = 0;
		this.lineTotal = 0;
		this.cost = 0;
		this.price = 0;
		this.qty = 0;	
	}
	
	public String getSyskey() {
		return syskey;
	}
	public void setSyskey(String syskey) {
		this.syskey = syskey;
	}
	public String getpId() {
		return pId;
	}
	public void setpId(String pId) {
		this.pId = pId;
	}
	public String getStkSK() {
		return stkSK;
	}
	public void setStkSK(String stkSK) {
		this.stkSK = stkSK;
	}	
	public String getStkCode() {
		return stkCode;
	}
	public void setStkCode(String stkCode) {
		this.stkCode = stkCode;
	}
	public String getStkDesc() {
		return stkDesc;
	}
	public void setStkDesc(String stkDesc) {
		this.stkDesc = stkDesc;
	}
	public double getPrice() {
		return price;
	}
	public void setPrice(double price) {
		this.price = price;
	}
	public double getQty() {
		return qty;
	}
	public void setQty(double qty) {
		this.qty = qty;
	}
	public String getUom() {
		return uom;
	}
	public void setUom(String uom) {
		this.uom = uom;
	}	
	public double getDiscAmt() {
		return discAmt;
	}
	public void setDiscAmt(double discAmt) {
		this.discAmt = discAmt;
	}
	public double getDiscPer() {
		return discPer;
	}
	public void setDiscPer(double discPer) {
		this.discPer = discPer;
	}	
	public double getAmt() {
		return amt;
	}
	public void setAmt(double amt) {
		this.amt = amt;
	}
	public double getNetAmt() {
		return netAmt;
	}
	public void setNetAmt(double netAmt) {
		this.netAmt = netAmt;
	}
	public double getLineTotal() {
		return lineTotal;
	}
	public void setLineTotal(double lineTotal) {
		this.lineTotal = lineTotal;
	}
	public double getCost() {
		return cost;
	}
	public void setCost(double cost) {
		this.cost = cost;
	}
}
